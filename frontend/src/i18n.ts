import { computed, ref } from 'vue';
import type { Role } from './types';

export type Locale = 'zh' | 'en';

type MessageKey = keyof typeof messages.zh;

const storedLocale = localStorage.getItem('tradelog_locale');
export const locale = ref<Locale>(storedLocale === 'en' ? 'en' : 'zh');

export const localeLabel = computed(() => (locale.value === 'zh' ? 'EN' : '中文'));

export function setLocale(value: Locale) {
  locale.value = value;
  localStorage.setItem('tradelog_locale', value);
}

export function toggleLocale() {
  setLocale(locale.value === 'zh' ? 'en' : 'zh');
}

export function t(key: MessageKey, vars: Record<string, string | number> = {}) {
  const template: string = messages[locale.value][key] || messages.zh[key] || key;
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
    template,
  );
}

export const rangeOptions = computed(() => [
  { label: t('range.today'), value: 'today' },
  { label: t('range.week'), value: 'week' },
  { label: t('range.month'), value: 'month' },
  { label: t('range.year'), value: 'year' },
  { label: t('range.all'), value: 'all' },
]);

export function roleLabel(role?: Role) {
  if (role === 'super_admin') return t('role.admin');
  return t('role.trader');
}

export function directionLabel(value: string) {
  return value === 'long' ? t('direction.long') : t('direction.short');
}

export function statusLabel(value: string) {
  if (value === 'profit') return t('status.profit');
  if (value === 'loss') return t('status.loss');
  return t('status.flat');
}

export function marginLabel(value: string) {
  return value === 'cross' ? t('margin.cross') : t('margin.isolated');
}

export function contractLabel(value: string) {
  return value === 'perpetual' ? t('contract.perpetual') : t('contract.delivery');
}

